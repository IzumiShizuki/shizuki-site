CREATE TABLE IF NOT EXISTS MDA_WALLPAPER_PROFILE (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT 'MDA_WALLPAPER_PROFILE.id 自增长ID',
    owner_user_id BIGINT NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.owner_user_id 所有者用户ID',
    title_text VARCHAR(255) NOT NULL DEFAULT '' COMMENT 'MDA_WALLPAPER_PROFILE.title_text 壁纸标题',
    visual_asset_id BIGINT NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.visual_asset_id 主视觉资源ID',
    scene_type VARCHAR(16) NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.scene_type 场景类型 STATIC/DYNAMIC/L2D',
    embedded_bgm_asset_id BIGINT NULL COMMENT 'MDA_WALLPAPER_PROFILE.embedded_bgm_asset_id 内嵌BGM资源ID',
    embedded_bgv_asset_id BIGINT NULL COMMENT 'MDA_WALLPAPER_PROFILE.embedded_bgv_asset_id 内嵌BGV资源ID',
    default_master_volume DECIMAL(5,2) NOT NULL DEFAULT 1.00 COMMENT 'MDA_WALLPAPER_PROFILE.default_master_volume 默认总音量',
    default_bgm_volume DECIMAL(5,2) NOT NULL DEFAULT 1.00 COMMENT 'MDA_WALLPAPER_PROFILE.default_bgm_volume 默认BGM音量',
    default_bgv_volume DECIMAL(5,2) NOT NULL DEFAULT 1.00 COMMENT 'MDA_WALLPAPER_PROFILE.default_bgv_volume 默认BGV音量',
    custom_schema_json JSON NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.custom_schema_json 动态参数Schema',
    custom_defaults_json JSON NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.custom_defaults_json 动态参数默认值',
    visibility_code TINYINT NOT NULL DEFAULT 1 COMMENT 'MDA_WALLPAPER_PROFILE.visibility_code 可见性编码 1私有 2公开',
    audit_status VARCHAR(32) NOT NULL DEFAULT 'PENDING_AUDIT' COMMENT 'MDA_WALLPAPER_PROFILE.audit_status 审核状态',
    import_source VARCHAR(16) NOT NULL COMMENT 'MDA_WALLPAPER_PROFILE.import_source 导入来源 PACKAGE/WORKSHOP',
    workshop_item_id VARCHAR(64) NULL COMMENT 'MDA_WALLPAPER_PROFILE.workshop_item_id Workshop条目ID',
    enabled_flag TINYINT(1) NOT NULL DEFAULT 1 COMMENT 'MDA_WALLPAPER_PROFILE.enabled_flag 启用标记',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'MDA_WALLPAPER_PROFILE.create_time 创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'MDA_WALLPAPER_PROFILE.update_time 更新时间',
    deleted_flag TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'MDA_WALLPAPER_PROFILE.deleted_flag 删除标记',
    version_num INT NOT NULL DEFAULT 0 COMMENT 'MDA_WALLPAPER_PROFILE.version_num 版本号',
    CONSTRAINT PK_MDA_WALLPAPER_PROFILE PRIMARY KEY (id),
    KEY IX_MDA_WALLPAPER_PROFILE_1 (owner_user_id, create_time),
    KEY IX_MDA_WALLPAPER_PROFILE_2 (visibility_code, audit_status, enabled_flag, create_time),
    KEY IX_MDA_WALLPAPER_PROFILE_3 (workshop_item_id),
    CONSTRAINT FK_MDA_WALLPAPER_PROFILE_1 FOREIGN KEY (visual_asset_id) REFERENCES MDA_ASSET(id),
    CONSTRAINT FK_MDA_WALLPAPER_PROFILE_2 FOREIGN KEY (embedded_bgm_asset_id) REFERENCES MDA_ASSET(id),
    CONSTRAINT FK_MDA_WALLPAPER_PROFILE_3 FOREIGN KEY (embedded_bgv_asset_id) REFERENCES MDA_ASSET(id)
) COMMENT='主页壁纸配置表';

CREATE TABLE IF NOT EXISTS MDA_WALLPAPER_IMPORT_JOB (
    id BIGINT NOT NULL AUTO_INCREMENT COMMENT 'MDA_WALLPAPER_IMPORT_JOB.id 自增长ID',
    owner_user_id BIGINT NOT NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.owner_user_id 发起用户ID',
    source_type VARCHAR(16) NOT NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.source_type 来源 PACKAGE/WORKSHOP',
    workshop_url VARCHAR(1024) NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.workshop_url Workshop链接',
    workshop_item_id VARCHAR(64) NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.workshop_item_id Workshop条目ID',
    status_text VARCHAR(32) NOT NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.status_text 导入状态',
    visibility_code TINYINT NOT NULL DEFAULT 1 COMMENT 'MDA_WALLPAPER_IMPORT_JOB.visibility_code 目标可见性 1私有 2公开',
    wallpaper_id BIGINT NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.wallpaper_id 导入成功后的壁纸ID',
    error_message VARCHAR(1024) NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.error_message 错误描述',
    fallback_hint VARCHAR(255) NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.fallback_hint 降级提示',
    payload_json JSON NOT NULL COMMENT 'MDA_WALLPAPER_IMPORT_JOB.payload_json 扩展上下文',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'MDA_WALLPAPER_IMPORT_JOB.create_time 创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'MDA_WALLPAPER_IMPORT_JOB.update_time 更新时间',
    deleted_flag TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'MDA_WALLPAPER_IMPORT_JOB.deleted_flag 删除标记',
    version_num INT NOT NULL DEFAULT 0 COMMENT 'MDA_WALLPAPER_IMPORT_JOB.version_num 版本号',
    CONSTRAINT PK_MDA_WALLPAPER_IMPORT_JOB PRIMARY KEY (id),
    KEY IX_MDA_WALLPAPER_IMPORT_JOB_1 (owner_user_id, create_time),
    KEY IX_MDA_WALLPAPER_IMPORT_JOB_2 (status_text, create_time),
    KEY IX_MDA_WALLPAPER_IMPORT_JOB_3 (workshop_item_id),
    CONSTRAINT FK_MDA_WALLPAPER_IMPORT_JOB_1 FOREIGN KEY (wallpaper_id) REFERENCES MDA_WALLPAPER_PROFILE(id)
) COMMENT='主页壁纸导入任务表';
