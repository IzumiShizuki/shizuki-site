-- PostgreSQL bootstrap for user/admin domain on fresh databases.
-- This keeps site-management APIs (user/group/quota/permission) available
-- even when the database was not migrated from MySQL beforehand.

CREATE TABLE IF NOT EXISTS USR_ACCOUNT (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username_code VARCHAR(64) NOT NULL,
    password_hash VARCHAR(255),
    nickname_text VARCHAR(128) NOT NULL,
    email_text VARCHAR(128),
    email_verified_flag SMALLINT NOT NULL DEFAULT 0,
    avatar_url_text VARCHAR(512),
    groups_json JSONB NOT NULL DEFAULT '[]'::jsonb,
    permissions_json JSONB NOT NULL DEFAULT '[]'::jsonb,
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX IF NOT EXISTS AK_USR_ACCOUNT_1 ON USR_ACCOUNT (username_code);
CREATE UNIQUE INDEX IF NOT EXISTS AK_USR_ACCOUNT_2 ON USR_ACCOUNT (email_text);
CREATE INDEX IF NOT EXISTS IX_USR_ACCOUNT_1 ON USR_ACCOUNT (email_text);
CREATE INDEX IF NOT EXISTS IX_USR_ACCOUNT_GROUPS_GIN ON USR_ACCOUNT USING GIN (groups_json);
CREATE INDEX IF NOT EXISTS IX_USR_ACCOUNT_GROUPS_GIN_ACTIVE ON USR_ACCOUNT USING GIN (groups_json) WHERE deleted_flag = 0;

DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.tables
        WHERE table_schema = 'public'
          AND table_name = 'usr_account'
    ) AND NOT EXISTS (
        SELECT 1
        FROM information_schema.columns
        WHERE table_schema = 'public'
          AND table_name = 'usr_account'
          AND column_name = 'avatar_url_text'
    ) THEN
        EXECUTE 'ALTER TABLE USR_ACCOUNT ADD COLUMN avatar_url_text VARCHAR(512)';
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS USR_PREFERENCE (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    preference_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_USR_PREFERENCE_1 UNIQUE (user_id),
    CONSTRAINT FK_USR_PREFERENCE_1 FOREIGN KEY (user_id) REFERENCES USR_ACCOUNT(id)
);

CREATE INDEX IF NOT EXISTS IX_USR_PREFERENCE_1 ON USR_PREFERENCE (create_time);

CREATE TABLE IF NOT EXISTS OAU_LOGIN (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    oauth_login_code VARCHAR(64) NOT NULL,
    provider_type VARCHAR(32) NOT NULL,
    redirect_uri_text VARCHAR(512) NOT NULL,
    state_code VARCHAR(128) NOT NULL,
    login_status VARCHAR(32) NOT NULL,
    login_scene VARCHAR(32) NOT NULL DEFAULT 'LOGIN',
    provider_user_code VARCHAR(128),
    user_id BIGINT,
    initiator_user_id BIGINT,
    error_memo VARCHAR(512),
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_OAU_LOGIN_1 UNIQUE (oauth_login_code),
    CONSTRAINT FK_OAU_LOGIN_1 FOREIGN KEY (user_id) REFERENCES USR_ACCOUNT(id)
);

CREATE INDEX IF NOT EXISTS IX_OAU_LOGIN_1 ON OAU_LOGIN (create_time);
CREATE INDEX IF NOT EXISTS IX_OAU_LOGIN_2 ON OAU_LOGIN (provider_type, login_scene, create_time);

CREATE TABLE IF NOT EXISTS OAU_BINDING (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    provider_type VARCHAR(32) NOT NULL,
    provider_user_code VARCHAR(128) NOT NULL,
    provider_login_code VARCHAR(128),
    provider_email_text VARCHAR(128),
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_OAU_BINDING_1 UNIQUE (provider_type, provider_user_code),
    CONSTRAINT FK_OAU_BINDING_1 FOREIGN KEY (user_id) REFERENCES USR_ACCOUNT(id)
);

CREATE INDEX IF NOT EXISTS IX_OAU_BINDING_1 ON OAU_BINDING (user_id);

CREATE TABLE IF NOT EXISTS USR_QUOTA_POLICY (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    policy_code VARCHAR(64) NOT NULL,
    group_code VARCHAR(32) NOT NULL,
    quota_code VARCHAR(64) NOT NULL,
    quota_value BIGINT NOT NULL,
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_USR_QUOTA_POLICY_1 UNIQUE (policy_code),
    CONSTRAINT AK_USR_QUOTA_POLICY_2 UNIQUE (group_code, quota_code)
);

CREATE INDEX IF NOT EXISTS IX_USR_QUOTA_POLICY_1 ON USR_QUOTA_POLICY (group_code);

CREATE TABLE IF NOT EXISTS USR_GROUP_PERMISSION (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_code VARCHAR(32) NOT NULL,
    permission_code VARCHAR(128) NOT NULL,
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_USR_GROUP_PERMISSION_1 UNIQUE (group_code, permission_code)
);

CREATE INDEX IF NOT EXISTS IX_USR_GROUP_PERMISSION_1 ON USR_GROUP_PERMISSION (group_code);

CREATE TABLE IF NOT EXISTS USR_PROVIDER_SECRET (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    provider_code VARCHAR(64) NOT NULL,
    cipher_text TEXT NOT NULL,
    key_mask VARCHAR(64),
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_USR_PROVIDER_SECRET_1 UNIQUE (user_id, provider_code),
    CONSTRAINT FK_USR_PROVIDER_SECRET_1 FOREIGN KEY (user_id) REFERENCES USR_ACCOUNT(id)
);

CREATE INDEX IF NOT EXISTS IX_USR_PROVIDER_SECRET_1 ON USR_PROVIDER_SECRET (provider_code);

CREATE TABLE IF NOT EXISTS AUD_LOG (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trace_code VARCHAR(64),
    user_id BIGINT,
    action_code VARCHAR(128) NOT NULL,
    resource_code VARCHAR(128),
    result_status VARCHAR(32) NOT NULL,
    error_code VARCHAR(64),
    cost_value BIGINT NOT NULL,
    create_time TIMESTAMP NOT NULL,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0
);

CREATE INDEX IF NOT EXISTS IX_AUD_LOG_1 ON AUD_LOG (trace_code);
CREATE INDEX IF NOT EXISTS IX_AUD_LOG_2 ON AUD_LOG (user_id);
CREATE INDEX IF NOT EXISTS IX_AUD_LOG_3 ON AUD_LOG (action_code);
CREATE INDEX IF NOT EXISTS IX_AUD_LOG_4 ON AUD_LOG (create_time);

CREATE TABLE IF NOT EXISTS AUD_EVENT_OUTBOX (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_type VARCHAR(64) NOT NULL,
    payload_json JSONB NOT NULL DEFAULT '{}'::jsonb,
    event_status VARCHAR(32) NOT NULL DEFAULT 'NEW',
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    processed_datetime TIMESTAMP,
    retry_count INT NOT NULL DEFAULT 0,
    next_retry_datetime TIMESTAMP,
    last_error_memo VARCHAR(1024)
);

CREATE INDEX IF NOT EXISTS IX_AUD_EVENT_OUTBOX_1 ON AUD_EVENT_OUTBOX (event_status, create_time);
CREATE INDEX IF NOT EXISTS IX_AUD_EVENT_OUTBOX_2 ON AUD_EVENT_OUTBOX (event_status, next_retry_datetime, create_time);

CREATE TABLE IF NOT EXISTS USR_GROUP (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_code VARCHAR(32) NOT NULL,
    display_name VARCHAR(64) NOT NULL,
    description_text VARCHAR(255),
    status_code VARCHAR(16) NOT NULL DEFAULT 'ACTIVE',
    built_in_flag SMALLINT NOT NULL DEFAULT 0,
    sort_num INT NOT NULL DEFAULT 0,
    create_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_flag SMALLINT NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT AK_USR_GROUP_1 UNIQUE (group_code)
);

CREATE INDEX IF NOT EXISTS IX_USR_GROUP_1 ON USR_GROUP (status_code, sort_num);

