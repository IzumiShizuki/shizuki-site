CREATE TABLE IF NOT EXISTS CTN_POST_PRESENTATION (
    id BIGINT NOT NULL AUTO_INCREMENT,
    post_id BIGINT NOT NULL,
    status_code VARCHAR(32) NOT NULL DEFAULT 'NOT_GENERATED',
    slidev_bucket_name VARCHAR(128) NULL,
    slidev_object_key VARCHAR(512) NULL,
    ppt_bucket_name VARCHAR(128) NULL,
    ppt_object_key VARCHAR(512) NULL,
    slide_count INT NOT NULL DEFAULT 0,
    template_version VARCHAR(32) NOT NULL DEFAULT 'v1',
    error_text TEXT NULL,
    generated_time DATETIME NULL,
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    deleted_flag TINYINT(1) NOT NULL DEFAULT 0,
    version_num INT NOT NULL DEFAULT 0,
    CONSTRAINT PK_CTN_POST_PRESENTATION PRIMARY KEY (id),
    CONSTRAINT AK_CTN_POST_PRESENTATION_1 UNIQUE (post_id),
    KEY IX_CTN_POST_PRESENTATION_1 (status_code),
    CONSTRAINT FK_CTN_POST_PRESENTATION_1 FOREIGN KEY (post_id) REFERENCES CTN_POST(id)
);
